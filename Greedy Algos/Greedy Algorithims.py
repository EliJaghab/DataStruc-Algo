# Greedy Algorithm builds up a solution by choosing the option that looks the best at every step
#Ex:
#1. Cashier returning 67 cents - pick the largest coin
#2. Fitting overlapping meetings as possible - at each step, schedule the meeting that ends earliest
#3. Minimum spanning tree in a graph - at each step, greedily pick the cheapest edge that reaches a new vertex

#Greedy solutions do not always provide an optimal solution
#Ex: Filling a duffel bag with different weights and values, highest value doesn't always produce the best results

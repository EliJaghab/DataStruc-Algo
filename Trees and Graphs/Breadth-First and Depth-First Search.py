# Breadth-First Search
# Like throwing a stone in the center of the pond, the nodes explored "ripple out" from the starting point

# Strengths:
#   Will find the shortest path between the starting point and any other reachable node
#   DFS will not necessarily find the shortest path

# Disadvantages:
#   BFS on a binary tree generally requires more memory than a DFS


# Depth-First Search
# Go deep as possible down one path before backing up and trying a different one
# Like walking through a corn maze - explore one path, hit a dead end, go back and try a different one

# Advantages:
#   Depth-first search on a binary tree generally requires less memory than breadth-first
#   Depth-first search can be easily implemented with recursion

# Disadvantages:
#   A DFS doesn't necessarily find the shorted path to a node, while BFS search does
